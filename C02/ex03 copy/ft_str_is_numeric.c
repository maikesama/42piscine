int		ft_str_is_numeric(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c >= '0' && c <= '9')
			i++;
		else
			return (0);
	}
	return (1);
}
